    private boolean productoExiste(TreeSet<Producto> lista, int codigo) { // Método verifica si existe el codigo ingresado en la lista

        for (Producto item : lista) {

            if (item.getCodigo() == codigo) {
                return (true);
            }
        }
        return (false);
    }

    private void resetearControles() { // Habilita los controles

        jtCodigo.setText("");
        jtDescripcion.setText("");
        jtPrecio.setText("");
        jtStock.setText("");
        jtCodigo.requestFocus();
    }

    private Producto buscarProducto(int cond, TreeSet<Producto> lista) { // Método que busca un producto por el código

        for (Producto item : lista) {
            if (item.getCodigo() == cond) {
                return (item);
            }
        }
        return (null);
    }

    private void actualizarProducto(Producto producto, TreeSet<Producto> lista) { // Método que actualiza un producto

        for (Producto item : lista) {

            if (item.getCodigo() == producto.getCodigo()) {
                item.setDescripcion(producto.getDescripcion());
                item.setPrecio(producto.getPrecio());
                item.setRubro(producto.getRubro());
                item.setStock(producto.getStock());
            }
        }
    }

    private void mostrarLista() { // Método muestra la lista por consola

        for (Producto item : lista) {
            System.out.println(item.toString());
        }

    }

