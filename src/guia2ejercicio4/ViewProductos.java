    private void jbtnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNuevoActionPerformed
        // Método que maneje el evento clic del boton "nuevo"

        int cod = Integer.parseInt(jtCodigo.getText());
        String descri = jtDescripcion.getText();
        double preci = Double.parseDouble(jtPrecio.getText());
        String catego = jbCategoria.getSelectedItem().toString();
        int stock = Integer.parseInt(jtStock.getText());

        if (buscarProducto(cod, lista) != null) { // consulto si existe el codigo en la lista
            JOptionPane.showMessageDialog(this, "El código ya existe!");
            jtCodigo.requestFocus();
        } else {
            producto = new Producto(cod, descri, preci, stock, catego);
            lista.add(producto);
            resetearControles();
        }

        mostrarLista();
    }//GEN-LAST:event_jbtnNuevoActionPerformed

    private void jbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBuscarActionPerformed
        // Método maneja el evento clic del boton "Buscar"
        resetearControles();

        try {
            int buscarCod = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el código a buscar"));
            producto = buscarProducto(buscarCod, lista);
            if (producto != null) {
                jtCodigo.setText(producto.getCodigo() + "");
                jtDescripcion.setText(producto.getDescripcion());
                jtPrecio.setText(producto.getPrecio() + "");
                jbCategoria.setSelectedItem(producto.getRubro());
                jtStock.setText(producto.getStock() + "");
            } else {
                JOptionPane.showMessageDialog(this, "El producto buscado no Existe!");
            }
        } catch (NumberFormatException error) {

        }
    }//GEN-LAST:event_jbtnBuscarActionPerformed

    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        // método que maneja el evento clic del Boton "Guardar"

        int cod = Integer.parseInt(jtCodigo.getText());
        String descri = jtDescripcion.getText();
        double preci = Double.parseDouble(jtPrecio.getText());
        String catego = jbCategoria.getSelectedItem().toString();
        int stock = Integer.parseInt(jtStock.getText());

        if (buscarProducto(cod, lista) != null) { // Consultamo si exite el articulo

            int respuesta = JOptionPane.showConfirmDialog(this, "El artículo ya existe ¿Desea actualizar el existente?", "Actualización", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (respuesta == JOptionPane.YES_OPTION) {
                producto = new Producto(cod, descri, preci, stock, catego);
                actualizarProducto(producto, lista);
                resetearControles();
            } else {
                resetearControles();
            }
        }

        mostrarLista();
    }//GEN-LAST:event_jbtnGuardarActionPerformed

    private void jbtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEliminarActionPerformed
        // Método que maneje el evento clic del boton "Eliminar"

        resetearControles();

        try {
            int buscarCod = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el código a buscar"));
            producto = buscarProducto(buscarCod, lista);
            if (producto != null) {

                int respuesta = JOptionPane.showConfirmDialog(this, "¿Desea Eliminar el articulo?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

                if (respuesta == JOptionPane.YES_OPTION) {
                    
                    lista.remove(new Producto(buscarCod,"",0.0,0,""));
                    resetearControles();
                }
            } else {
                JOptionPane.showMessageDialog(this, "El producto buscado no Existe!");
            }
        } catch (NumberFormatException error) {

        }
        
        mostrarLista();
    }//GEN-LAST:event_jbtnEliminarActionPerformed


